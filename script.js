function switchLang(lang) {
  const c = content[lang];

  if (document.getElementById("goName")) document.getElementById("goName").textContent = c.goName;
  if (document.getElementById("chaseName")) document.getElementById("chaseName").textContent = c.chaseName;

  // ✅ 更新角色关系描述文字
  if (document.getElementById("relationship-text")) {
    document.getElementById("relationship-text").innerHTML = c.relationshipText.replace(/\n/g, "<br>");
  }

  // ✅ 更新三张关系图配文 captions[]
  if (c.captions && Array.isArray(c.captions)) {
    for (let i = 0; i < 3; i++) {
      const cap = document.getElementById("caption0" + (i + 1));
      if (cap) cap.textContent = c.captions[i];
    }
  }

  if (document.getElementById("noticeText")) document.getElementById("noticeText").textContent = c.noticeText;

  // ✅ 插图页面配文（如有的话，继续保留不影响）
  for (let i = 1; i <= 6; i++) {
    const cap = document.getElementById("caption" + i);
    if (cap && c["caption" + i]) {
      cap.textContent = c["caption" + i];
    }
  }

  if (document.getElementById("backLink")) {
    document.getElementById("backLink").textContent = c.backText;
  }

  if (document.getElementById("relationship-title")) {
    document.getElementById("relationship-title").textContent = c.relationshipTitle;
  }

  if (document.getElementById("notice-title")) {
    document.getElementById("notice-title").textContent = c.noticeTitle;
  }

  if (document.getElementById("suitRefBtn")) {
    document.getElementById("suitRefBtn").textContent = c.suitBtnText;
  }
}


window.onload = () => switchLang("zh");

